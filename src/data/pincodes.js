export function getRegionByPincode(pincode) {
  const normalized = pincode.trim();
  if (pincodeData[normalized]) {
    return pincodeData[normalized];
  }
  
  // Fuzzy match - check if first 3 digits match curated list
  const prefix3 = normalized.substring(0, 3);
  for (const [key, value] of Object.entries(pincodeData)) {
    if (key.startsWith(prefix3)) {
      return value;
    }
  }

  // Broad state-level mapping using first 2 digits
  const prefix2 = normalized.substring(0, 2);
  if (statePrefixes[prefix2]) {
    return statePrefixes[prefix2];
  }

  return { region: 'India', language: 'Hindi', state: 'Unknown' };
}
